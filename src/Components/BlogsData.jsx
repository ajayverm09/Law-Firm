// Function to get unique categories
export const getCategories = () => {
  const categories = [...new Set(blogsData.map(blog => blog.category))];
  return categories;
};

// Function to get unique tags
export const getTags = () => {
  const tagsSet = new Set();
  blogsData.forEach(blog => {
    blog.tags.forEach(tag => tagsSet.add(tag));
  });
  return [...tagsSet];
};

// Function to get blogs by category
export const getBlogsByCategory = (category) => {
  return blogsData.filter(blog => blog.category === category);
};

// Function to get related blogs
export const getRelatedBlogs = (currentBlogId, limit = 3) => {
  const currentBlog = blogsData.find(blog => blog.id === currentBlogId);
  if (!currentBlog) return [];
  
  return blogsData
    .filter(blog => blog.id !== currentBlogId && blog.category === currentBlog.category)
    .slice(0, limit);
};

// Function to search blogs
export const searchBlogs = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return blogsData.filter(blog => 
    blog.title.toLowerCase().includes(lowercaseQuery) ||
    blog.excerpt.toLowerCase().includes(lowercaseQuery) ||
    blog.content.toLowerCase().includes(lowercaseQuery) ||
    blog.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

// Function to get latest blogs
export const getLatestBlogs = (limit = 3) => {
  return [...blogsData].sort((a, b) => new Date(b.date) - new Date(a.date)).slice(0, limit);
};

// Function to get popular blogs (for demo, we'll use the first 3)
export const getPopularBlogs = (limit = 3) => {
  return blogsData.slice(0, limit);
};

export default blogsData;